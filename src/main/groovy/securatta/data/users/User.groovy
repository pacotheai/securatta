package securatta.data.users

/**
 * Represents a user. A user is an authenticated individual.
 *
 * @since 0.1.2
 */
class User {

    /**
     * Human friendly name
     *
     * @since 0.1.2
     */
    String name

    /**
     * User username, should be unique within the system
     *
     * @since 0.1.2
     */
    String username
}
