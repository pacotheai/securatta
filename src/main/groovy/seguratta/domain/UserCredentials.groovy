package seguratta.domain

/**
 * Authentication credentials for a given user
 *
 * @since 0.1.0
 */
class UserCredentials {
    /**
     * Username to identify the user within the system
     *
     * @since 0.1.0
     */
    String username

    /**
     * Password assigned to the user within the system
     *
     * @since 0.1.0
     */
    String password
}
